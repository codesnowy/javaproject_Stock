package togit;

public class Helloworld {
	public static void main(String[] args) {
		//단축키 [Ctrl + D] 커서가 위치한 한 줄 삭제
	  System.out.println("Hello World!");

	  //단축키 [Ctrl + Space] : 자동 완성
	  System.out.println();
	  
	//단축키 [Ctrl + Alt + 화살표 아래] : copy line
	  System.out.println();
	  System.out.println();
	 
	//단축키 [Crtl + Y] : redo
	//단축키 [Crtl + Z] : undo
	}
}
